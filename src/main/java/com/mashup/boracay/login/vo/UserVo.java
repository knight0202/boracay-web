package com.mashup.boracay.login.vo;

public class UserVo {
	private int memberSec;
	private String memberId;
	private String memberPw;
	private String memberKind;
	
	
	public int getMemberSec() {
		return memberSec;
	}
	public void setMemberSec(int memberSec) {
		this.memberSec = memberSec;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getMemberPw() {
		return memberPw;
	}
	public void setMemberPw(String memberPw) {
		this.memberPw = memberPw;
	}
	public String getMemberKind() {
		return memberKind;
	}
	public void setMemberKind(String memberKind) {
		this.memberKind = memberKind;
	}
	
	
}
